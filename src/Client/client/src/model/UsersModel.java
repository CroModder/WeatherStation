package model;

import java.util.Date;

public class UsersModel {

	private Integer id;
	private String name;
    private Date date;
    private String role;
    
    public UsersModel(Integer id, String name, Date date , String role) {
    
    	this.id = id;
        this.name = name;
        this.date = date;
        this.role = role;
    	
    }

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}
    
    
}
